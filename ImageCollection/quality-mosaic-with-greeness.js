// Map Functions
function addIndices(image) {
  //EVI algorithm taken from: http://landsat.usgs.gov/documents/si_product_guide.pdf
  //EVI = (Band 4 – Band 3) / (Band 4 + 6 * Band 3 – 7.5 * Band 1 + 1)
  var nir = image.select(['nir']).multiply(10000);
  var red = image.select(['red']).multiply(10000);
  var evi = (nir.subtract(red)).divide((nir))
  image = image.addBands(nir.subtract(red).divide(nir).select([0],['evi']));
  //NDVI
  image = image.addBands(image.normalizedDifference(['nir', 'red']).select([0],['ndvi']));
  
  return image;
}

function addDateBand(image) {
  // Returns Unix Time(Epoch) in seconds
  // https://en.wikipedia.org/wiki/Unix_time
   return image.addBands(ee.Image.constant(
      ee.Date(image.get('DATE_ACQUIRED')).millis()).divide(1000).long().select([0],['epoch']));
}

